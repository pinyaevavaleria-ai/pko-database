/**
 * Убирает организационную форму (НАО, ООО, АО, ПАО, ЗАО)
 * и префикс «ПКО» / «ПРОФЕССИОНАЛЬНАЯ КОЛЛЕКТОРСКАЯ ОРГАНИЗАЦИЯ»
 * из отображаемого названия компании.
 *
 * Данные остаются нетронутыми — функция только для display-слоя.
 */
export function stripOrgForm(name: string): string {
  let s = name.trim();
  // 1. Убираем ведущую орг. форму: НАО, ООО, ПАО, АО, ЗАО
  s = s.replace(/^(НАО|ООО|ПАО|АО|ЗАО)\s*/i, '');
  // 2. Убираем «ПРОФЕССИОНАЛЬНАЯ КОЛЛЕКТОРСКАЯ ОРГАНИЗАЦИЯ» (полное написание)
  s = s.replace(/^"?ПРОФЕССИОНАЛЬНАЯ КОЛЛЕКТОРСКАЯ ОРГАНИЗАЦИЯ\s*/i, '');
  // 3. Убираем «ПКО» (сокращение)
  s = s.replace(/^"?ПКО\s*/i, '');
  // 4. Убираем «ДА » (для ООО ПКО "ДА "ФЕМИДА")
  // Не убираем — это часть названия
  // 5. Чистим оставшиеся кавычки по краям
  s = s.replace(/^["\s]+/, '').replace(/["\s]+$/, '');
  return s;
}
